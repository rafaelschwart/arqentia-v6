export function getLang() {
  try {
    const stored = localStorage.getItem('arq_lang');
    if (stored === 'es' || stored === 'en') return stored;
  } catch {}
  return 'en';
}

export function setLang(lang) {
  const v = lang === 'es' ? 'es' : 'en';
  try { localStorage.setItem('arq_lang', v); } catch {}
  document.documentElement.setAttribute('lang', v);
  document.dispatchEvent(new CustomEvent('arq:lang', { detail: v }));
}

export function t(key, vars = {}) {
  const lang = getLang();
  const tpl = STRINGS[lang]?.[key] ?? STRINGS.en[key] ?? key;
  return String(tpl).replace(/\{(\w+)\}/g, (_, k) => String(vars[k] ?? ''));
}

export function localize(value) {
  if (value && typeof value === 'object' && !Array.isArray(value)) {
    const lang = getLang();
    return value[lang] ?? value.en ?? '';
  }
  return value;
}
